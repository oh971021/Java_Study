import java.util.Date;

public class UMain_2 {
	public static void main(String[] args) {
		
		// 시간과 날짜 (Datetype)
		// java.util에 있는거
		Date d = new Date();
		System.out.println(d); // 현재의 정보
		
		/*
			Date -> String
			y(연) M(월) d(일) a(오전/오후) h(시) m(분) s(초)
		
		*/
		
		// 시간
		System.out.println(d.getHours());
		// 분
		
		// 초
		
		
		
		
	}
}
