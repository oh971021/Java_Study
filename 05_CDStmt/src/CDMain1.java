// Conditional Stmt (조건문
// 조건문 (if)

// if (조건식) {
//		조건이 참일 때 실행 되는 문장
// }


public class CDMain1 {
	public static void main(String[] args) {
		// if문, else (둘 중 하나, 맞나 안맞나)
		
		int age = 70;
		
		// 나이가 70 이상이면 "안녕하십니까"
		// 아니면 "어리시네요" 출력
		
		if (age >= 70) {
			System.out.println("안녕하십니까");
		} else {
			System.out.println("어리시네요");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
